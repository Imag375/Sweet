package ru.mirea;

public class Guide {
    String birth;
    int check;
    String name;
    String overdraft;
    String probation;
    float salary;
    String telephone;

    Guide(String birth, int check, String name, String overdraft, String probation, float salary, String telephone) {
        this.birth = birth;
        this.check = check;
        this.name = name;
        this.overdraft = overdraft;
        this.probation = probation;
        this.salary = salary;
        this.telephone = telephone;
    }

}
